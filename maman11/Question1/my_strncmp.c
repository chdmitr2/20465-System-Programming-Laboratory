/*Compare n chars of two strings*/
#include "my_string.h"
#include <string.h>
#include <stdio.h>
#include <stdlib.h>
int my_strncmp(char* x,char* y,int n)/*Take two strings and compare their n chars*/
{
int i=0;
for(i=0;i<n;i++)/*loop that passing n chars in two strings*/
{
if(x[i]<y[i])/*compare char from two strings in specific index i */
    return 1;
else if (x[i]>y[i])
    return 1;
}
return 0;/*return 0 if n chars identical of two strings*/
}
